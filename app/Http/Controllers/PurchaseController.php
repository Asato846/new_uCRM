    {
	Purchase::create([
            'session_id' => session()->getId(),
            'item_id' => $request->id,
            'quantity' => $request->quantity,
	    'purchase_status' => 0
        ]);
        return to_route('items.index')
        ->with([
            'message' =>'カートへ追加しました。',
            'status' =>'success'
        ]);
    }

